    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String jenisKelamin = "Laki-Laki";
        if (this.rbPerempuan.isSelected()) {
            jenisKelamin = "Perempuan";
        }
        boolean hasil = false;
        hasil = kc.insert(
                txtNik.getText(),
                txtNama.getText(),
                txtAlamat.getText(),
                txtAgama.getText(),
                jenisKelamin,
                txtTglLahir.getDate().getTime() + "",
                txtTglKerja.getDate().getTime() + "",
                Short.parseShort(txtSisaCuti.getText()), txtNik.isEnabled());
        txtNik.setEnabled(true);
        String pesan = "Gagal menambahkan data";
        if (hasil) {
            try {
                pesan = "Berhasil menambahkan Data";
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        JOptionPane.showMessageDialog(this, pesan);
        kc.bindingAll(tblKaryawan, header);
        reset();
    }//GEN-LAST:event_btnSimpanActionPerformed

