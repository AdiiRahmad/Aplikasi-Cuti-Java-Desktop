    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int i = JOptionPane.showConfirmDialog(this, "Apakah Anda Yakin Ingin dihapus?");
        if (i == 0) {
            try {
                String pesan = "Gagal hapus";
                boolean hasil = kc.delete(txtNik.getText());
                if (hasil) {
                    pesan = "Data Berhasil di hapus";
                    reset();
                }
                JOptionPane.showMessageDialog(this, pesan);
                kc.bindingAll(tblKaryawan, header);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKaryawanMouseClicked
        txtNik.setText("" + tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 0) + "");
        txtNama.setText("" + tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 1) + "");
        txtAlamat.setText("" + tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 2) + "");
        txtAgama.setText("" + tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 3) + "");
        String jk = "" + tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 4)+ "";
        System.out.println(jk);
        if ("Laki-Laki".equals(jk)) {
            rbLakiLaki.setSelected(rootPaneCheckingEnabled);
        }else {
            rbPerempuan.setSelected(rootPaneCheckingEnabled);
        }
        txtTglLahir.setDate((Date) tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 5));
        txtTglKerja.setDate((Date) tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 6));
        txtSisaCuti.setText("" + tblKaryawan.getValueAt(tblKaryawan.getSelectedRow(), 7) + "");
        btnSimpan.setEnabled(true);
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tblKaryawanMouseClicked

