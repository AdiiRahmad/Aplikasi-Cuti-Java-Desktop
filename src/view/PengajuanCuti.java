    private void txtLamaCutiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLamaCutiActionPerformed
        Date tglMulai = txtTglMulai.getDate();
        Date tglSelesai = txtTglSelesai.getDate();

        //Konversi
        Long mulai = tglMulai.getTime();
        Long Selesai = tglSelesai.getTime();
        Long Selisih = Selesai - mulai;
        Long JangkaWaktu = Selisih / (24 * 60 * 60 * 1000) + 1;

        txtLamaCuti.setText(JangkaWaktu.toString());
        if (JangkaWaktu > 12) {
            JOptionPane.showMessageDialog(panel,
                    "Cuti Tidak Boleh lebih dari 12 hari... Hubungi Atasan", "Peringatan", JOptionPane.ERROR_MESSAGE);
        } else {

        }
        txtLamaCuti.setEnabled(false);
    }//GEN-LAST:event_txtLamaCutiActionPerformed

