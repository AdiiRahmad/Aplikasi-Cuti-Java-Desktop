    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        boolean hasil = false;
        hasil = dc.insertPengajuanCuti(
                txtIdDetailCuti.getText(),
                cmbNik.getSelectedItem().toString(),
                cmbJenisCuti.getSelectedItem().toString(),
                txtKeterangan.getText(),
                txtTglMulai.getDate().getTime() + "",
                txtTglSelesai.getDate().getTime() + "",
                Short.parseShort(txtLamaCuti.getText()));

        String pesan = "Gagal menambahkan data";
        if (hasil) {
            try {
                pesan = "Berhasil menambahkan Data";
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        JOptionPane.showMessageDialog(this, pesan);
        reset();
    }//GEN-LAST:event_btnSimpanActionPerformed

